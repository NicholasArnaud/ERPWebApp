    public class OrderWeight
    {
        [Column(TypeName = "decimal(18,4)"), Range(1, double.MaxValue, ErrorMessage = "{0} must be greater or equal to {1}.")]
        public decimal value { get; set; } = 1.00m;
        /// <summary>
        /// units of weight. Allowed units are: "pounds", "ounces", or "grams"
        /// </summary>
        [System.Text.Json.Serialization.JsonConverter(typeof(JsonStringEnumConverter<Units>))]
        public Units units { get; set; } = Units.ounces;
        /// <summary>
        /// (read only) A numeric value that is equivalent to the above units field.
        /// </summary>
        [Display(Name = "Weight Unit")]
        public int WeightUnits { get; set; } = (int)Units.ounces;
        public enum Units
        {
            pounds = 0,
            ounces = 1,
            grams = 2,
        }


    }
    public class OrderDimensions
    {
        [Display(Name = "Length"), Column(TypeName = "decimal(18,4)"), Range(1, double.MaxValue, ErrorMessage = "{0} must be greater or equal to {1}.")]
        public decimal length { get; set; } = 1.00m;

        [Display(Name = "Height"), Column(TypeName = "decimal(18,4)"), Range(1, double.MaxValue, ErrorMessage = "{0} must be greater or equal to {1}.")]
        public decimal height { get; set; } = 1.00m;

        [Display(Name = "Width"), Column(TypeName = "decimal(18,4)"), Range(1, double.MaxValue, ErrorMessage = "{0} must be greater or equal to {1}.")]
        public decimal width { get; set; } = 1.00m;

        /// <summary>
        /// Units of measurement. Allowed units are: "inches", or "centimeters"
        /// </summary>
        [Display(Name = "Dimension Unit")]
        public Units units { get; set; } = Units.inches;

        public enum Units
        {
            inches,
            centimeters
        }
    }
